 function sum (x , y){
    return Number(x) + Number(y)
}

 function rest (x , y){
    return Number(x)  - Number(y)
}
 function mult (x , y){
    return Number(x) * Number(y)
}
 function div (x , y){
    return Number(x) / Number(y)
}

exports.sum=sum
exports.rest=rest
exports.mult=mult
exports.div=div
